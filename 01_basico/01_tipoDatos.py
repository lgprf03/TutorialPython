# Creacion de un main en python
if __name__ == "__main__":
    # Entero
    print(type(1))

    # Decimal, flotante
    print(type(23.23))

    # Booleano
    print(type(True))
    print(type(False))
    
    # Caracter
    print(type('a'))
    
    # Cadena
    print(type("cadena"))

    # Nulo
    print(type(None))

    # Arreglo, lista, objeto
    print(type([1, 2, 3]))
    print(type([1, 12.1, "a", True]))

    # Diccionario
    print(type({"clave": "valor"}))