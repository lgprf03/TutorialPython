# Creacion de un main en python
if __name__ == "__main__":
    """
    Operadores aritmeticos:

    +: suma
    -: resta
    *: multiplicacion
    /: division
    //: division entera
    %: modulo
    **: potencia
    """
    num1 = 2
    num2 = 5

    print(num1 + num2)
    print(num1 - num2)
    print(num1 * num2)
    print(num1 / num2)
    print(num1 // num2)
    print(num1 % num2)
    print(num1 ** num2)
    """
    Operadores de asignacion:

    =: asigna
    +=: suma y asigna
    -=: resta y asigna
    *=: multiplica y asigna
    /=: divide y asigna
    %=: modula Y asigna
    **=: potencia y asigna
    """
    num1 += 5
    print(num1)
    num1 -= 1
    print(num1)
    num1 *= 2
    print(num1)
    num1 /= 2
    print(num1)
    num1 %= 2
    print(num1)
    num1 **= 0
    print(num1)